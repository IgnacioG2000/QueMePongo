package domain;

//Creamos la clase color para el modo RGB (porcentaje de rojo, porcentaje de verde y porcentaje de azul)
public class Color {
    int rojo;
    int verde;
    int azul;

    public Color(int rojo, int verde, int azul) {
        this.rojo = rojo;
        this.verde = verde;
        this.azul = azul;
    }
}
